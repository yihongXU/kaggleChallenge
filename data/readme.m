"small_dataset" is a folder for kaggle dataset from https://www.kaggle.com/c/dog-breed-identification/data
"data" is a well seperated dataset (including labels information) for images from stanford dataset http://vision.stanford.edu/aditya86/ImageNetDogs/

"res_incep" contains bottlenecks exctract from Resnet_Inception v2 for validation and training

"test_res_incep" contains bottlenecks exctract from Resnet_Inception v2 for testing

"models" folder contains trained fully connected layer models
(resnet_Inception get the best result for now)
